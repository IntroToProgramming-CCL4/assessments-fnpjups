age = 16 #Set value
if age < 2:
    print("You are a baby :0") #Prints if they are less than 2
elif age < 4:
    print("You are a toddler") #Prints if input is less than 4
elif age < 13:
    print("You are a kid") #Prints if age is less than 13
elif age < 20:
    print("You're a teenager, just like me :D") #The code should print this output
elif age < 65:
    print("You are an adult")  #Prints when age is less than 65
else:
    print("You're an elder") #Prints if no conditions were met